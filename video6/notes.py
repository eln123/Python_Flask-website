# video 6 is about message flashing

# "message flashing" means showing info on the previous page on the next page when something happens on the GUI (gue-ee)
# - e.g you login, then on the next page is says "logged in successfully"
# - e.g. you logout, then a message is flashed "you logged out successfully"
# 
# this gives the user more interaction/feedback with the page so they know what's going on
# as opposed to having to change the whole page entirely or passing through other variables to show on the screen,
# you can just flash a message

# to do this, just import flash from Flask 